CREATE TABLE client
       (id INT primary key,
        name text,
        village text );


CREATE TABLE items
    (id INT primary key,
     name text,
     UNIQUE (id));

CREATE TABLE weapons
    (id INT,
     name VARCHAR(30),
     material VARCHAR(30),
     FOREIGN KEY (id) REFERENCES Items(id) ON DELETE CASCADE);

CREATE TABLE armors
       (id INT,
        name text,
        type VARCHAR(14),
        material VARCHAR(14),
        FOREIGN KEY (id) REFERENCES Items(id) ON DELETE CASCADE);

CREATE TABLE animals
    (id INT,
     name text,
     type VARCHAR(14),
     sexe VARCHAR(15),
     FOREIGN KEY (id) REFERENCES Items(id) ON DELETE CASCADE);

CREATE TABLE potions
    (id INT,
      name text,
      effect text,
       FOREIGN KEY (id) REFERENCES Items(id) ON DELETE CASCADE);

CREATE TABLE villages
    (name text primary key,
     coordX INT,
     coordY INT);


